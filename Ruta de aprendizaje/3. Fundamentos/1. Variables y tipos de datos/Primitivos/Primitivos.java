package VariablesYTiposDeDatos;

/**
 * Variables y tipos de datos: Primitivos
 * @author TitoDev
 */

public class Primitivos {

    public static void main(String[] args) {

        /* byte: es un tipo de dato entero que ocupa 8 bits. Su rango es de -128 a 127. */
        byte miByte = 10;

        /* short: es un tipo de dato entero que ocupa 16 bits. Su rango es de -32,768 a 32,767. */
        short miShort = 1000;

        /* int: es un tipo de dato entero que ocupa 32 bits. Su rango es de -2,147,483,648 a 2,147,483,647. */
        int miInt = 1000000;

        /* long: es un tipo de dato entero que ocupa 64 bits. Su rango es de -9,223,372,036,854,775,808 a 9,223,372,036,854,775,807. */
        long miLong = 1000000000L;

        /* float: es un tipo de dato de punto flotante que ocupa 32 bits. Su rango es de aproximadamente 1.4E-45 a 3.4E38. */
        float miFloat = 3.1416f;

        /* double: es un tipo de dato de punto flotante que ocupa 64 bits. Su rango es de aproximadamente 4.9E-324 a 1.8E308. */
        double miDouble = 3.141592653589793;

        /* boolean: es un tipo de dato que puede ser verdadero o falso. Su tamaño es de 1 bit. */
        boolean miBoolean = true;

        /* char: es un tipo de dato que representa un carácter único. Su rango es de '\u0000' (o 0) a '\uffff' (o 65,535). */
        char miChar = 'A';
    }
}
